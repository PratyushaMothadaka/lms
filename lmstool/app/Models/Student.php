{
    use HasFactory, Notifiable;

    // protected $table = 'students';
    // protected $guard = 'students';

    protected $fillable = [
        'name',
        'email',
        'password',
    ];

    protected $hidden = [
        'password',
    ];

    protected $casts = [
        'email_verified_at' => 'datetime',
    ];
}

